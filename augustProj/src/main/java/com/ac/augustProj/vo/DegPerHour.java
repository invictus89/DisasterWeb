package com.ac.augustProj.vo;

public class DegPerHour {
	private String hour;
	private int deg1;
	private int deg2;
	private int deg3;
	
	public String getHour() {
		return hour;
	}
	public int getDeg1() {
		return deg1;
	}
	public int getDeg2() {
		return deg2;
	}
	public int getDeg3() {
		return deg3;
	}
	public void setHour(String hour) {
		this.hour = hour;
	}
	public void setDeg1(int deg1) {
		this.deg1 = deg1;
	}
	public void setDeg2(int deg2) {
		this.deg2 = deg2;
	}
	public void setDeg3(int deg3) {
		this.deg3 = deg3;
	}
	
	
}
